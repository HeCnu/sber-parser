package dto;

public class Polygon {
    private Double maxLat;
    private Double minLat;
    private Double maxLon;
    private Double minLon;

    public Polygon(Double maxLat, Double minLat, Double maxLon, Double minLon) {
        this.maxLat = maxLat;
        this.minLat = minLat;
        this.maxLon = maxLon;
        this.minLon = minLon;
    }


}
